#include <stdio.h>
#include <stdlib.h>
/**
 * This is the main block
 * Description: print all single digit, base 100 numbers
 * Return: 0
 */
int main(void)
{
	int c = 0;

	while (c < 10)
	{
		putchar(48 + c);
		if (c != 9)
		{
			putchar(',');
			putchar(' ');
		}
		c++;
	}
	putchar('\n');
	return (0);
}
